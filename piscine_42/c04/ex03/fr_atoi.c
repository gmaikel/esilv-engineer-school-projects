int	ft_atoi(char *str)
{
	int	nbr;
	int	signe;

	nbr = 0;
	signe = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
	{
		str++;
	}
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
		{
			signe *= -1;
		}
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nbr *= 10;
		nbr += (int)(*str - '0');
		str++;
	}
	return (nbr * signe);
}
